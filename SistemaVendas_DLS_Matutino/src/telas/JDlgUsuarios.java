    public void beanTela(Usuarios UsuariosPesquisa){
        usuario = UsuariosPesquisa;
        calendario.setTime(usuario.getDataNascimento());
        calendario.add(Calendar.DATE, 1);
        usuario.setDataNascimento(calendario.getTime());

        jTxtNome.setText(usuario.getNome());
        jTxtCodigo.setText(util.intStr(usuario.getIdusuarios()));
        jTxtApelido.setText(usuario.getApelido());
        jFmtCpf.setText(usuario.getCpf());
        jFmtDataNascimento.setText(util.dataStr(usuario.getDataNascimento()));
        jPwdSenha.setText(usuario.getNome());
        jCboNivel.setSelectedIndex(usuario.getNivel());
        if (usuario.getAtivo().equals("S")) {
            jChbAtivo.setSelected(true);
        }else {
            jChbAtivo.setSelected(false);
        }
    }
    
    public void telaBean(){
        Usuarios usuarios = new Usuarios();
        usuarios.setIdusuarios(util.strInt(jTxtCodigo.getText()));
        usuarios.setNome(jTxtNome.getText());
        usuarios.setApelido(jTxtApelido.getText());
        usuarios.setCpf(jFmtCpf.getText());
        usuarios.setSenha(jPwdSenha.getText());
        usuarios.setNivel(jCboNivel.getSelectedIndex());
        usuarios.setDataNascimento(util.strData(jFmtDataNascimento.getText()));
        if (jChbAtivo.isSelected()) {
            usuarios.setAtivo("S");
        } else {
            usuarios.setAtivo("N");
        }
        if (incluir) {
            usuariosDAO.insert(usuarios);
        } else {
            usuariosDAO.update(usuarios);
        }
    }

