    private void jBtnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnExcluirActionPerformed
        
        int resp;
        if (usuario != null) {
            resp = util.perguntar("Confirmar e exclusão do registro?", "Confirmar e exclusão do registro?", JOptionPane.YES_NO_OPTION);
            if (resp == JOptionPane.YES_OPTION) {
                usuariosDAO.delete(usuario);
                util.limparNovo(compCampos);
                usuario = null;
            }
        }else{
            util.mostrar("faça uma pesquisa primeiro");
        }
    }//GEN-LAST:event_jBtnExcluirActionPerformed

