    private void jBtnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnConsultarActionPerformed
        // TODO add your handling code here:
        
	String nome = jTxtPesquisar.getText();
	boolean semRegistro = true;
        ArrayList<Usuarios> lista = usuarioDao.listAll();
        usuarioDao = new UsuariosDAO();
        ArrayList lista2 = new ArrayList();
        usuariosPesquisa = new UsuariosPesquisa();
        for(Usuarios usuarios : lista){
            if(usuarios.getNome().equals(nome)){
                lista2.add(usuarioDao.list(usuarios.getIdusuarios()));
                usuariosPesquisa.setList(lista2);
                jTable2.setModel(usuariosPesquisa);
		semRegistro = false;
            }
        }
	if(semRegistro){
            util.mostrar("Esse registro n√£o existe!");
        }
    }//GEN-LAST:event_jBtnConsultarActionPerformed

